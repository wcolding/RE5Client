std::vector<RE5Client::APRE5Entry> APRE5Entries;
int activeAPRE5Entry = 0;

void RE5Client::GetAPRE5Entries() {
    APRE5Entries.clear();

    for (const auto& file : std::filesystem::directory_iterator(std::filesystem::current_path()))
    {
        auto ext = file.path().extension().string();

        if (ext == ".apre5")
        {
            RE5Client::APRE5Entry newEntry;
            newEntry.fileName = file.path().filename().string();

            int result = RE5MemTools::LocationData::DecompressJSONFromFile(newEntry.fileName, newEntry.dataJSON, newEntry.header);
            if (result == LOC_DATA_OK) {
                APRE5Entries.push_back(newEntry);
            }
        }
    }

    if (!APRE5Entries.empty())
        activeAPRE5Entry = 0;
}

RE5Client::APRE5Entry RE5Client::GetActiveEntry() {
    return APRE5Entries[activeAPRE5Entry];
}


int RE5Client::GetLocationID(std::string levelARC, int index) {
    if (LocationMap.contains(levelARC)) {
        if (LocationMap[levelARC].size() <= index)
            return -1;
        return LocationMap[levelARC][index];
    }
}

RE5MemTools::LocationData::Location* RE5Client::GetCurrentLocation(std::string levelARC, int index, std::vector<RE5MemTools::LocationData::Location>& locationData) {
    if (LocationMap.contains(levelARC)) {
        auto locSet = LocationMap[levelARC];
        if (index > (locSet.size() - 1))
            return nullptr;

        int locID = locSet[index];
        
        for (int i = 0; i < locationData.size(); i++) {
            if (locationData[i].id == locID)
                return &locationData[i];
        }
    }

    return nullptr;
}